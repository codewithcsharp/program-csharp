namespace Diffrent.Practice.Solution.Pattern.SOLID.OCP.ProblemStatement
{
    public class MathLibraryProblemStatement
    {
        public int Addition(int a, int b)
        {
            return a + b;
        }

        public int Substraction(int a, int b)
        {
            return a - b;
        }

        public double Division(int a, int b)
        {
            return (double)a / b;
        }
    }
}
