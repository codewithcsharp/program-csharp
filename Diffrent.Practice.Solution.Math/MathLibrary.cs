namespace Diffrent.Practice.Solution.Math
{
    public class MathLibrary
    {
        public int Addition(int a, int b)
        {
            return a + b;
        }

        public int Substraction(int a, int b)
        {
            return a - b;
        }

        public double Division(int a, int b)
        {
            return (double)a / b;
        }
    }
}
